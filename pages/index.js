export async function getStaticProps({ preview, previewData }) {
  if (preview) {
    return getGithubPreviewProps({
      ...previewData,
      fileRelativePath: "content/new.md",
      parse: parseMarkdown,
    });
  }
  return {
    props: {
      file: {
        data: {
          markdownBody: "",
        },
      },
    },
  };
}
